void timer0_init( void ) {
	TCCR0B = _BV(TSM) | _BV(PSR0);
	TCCR0B = _BV(TSM) | _BV(PSR0) | _BV(CS02);
	TCCR0A = _BV(0);
	TCNT0L = 0;
	OCR0A = 47; // ~ 1ms
	TIFR = _BV(OCF0A);
	TIMSK &= ~_BV(OCIE0A);
	TCCR0B &= ~_BV(TSM);
}

void timer0_poll( void ) {
	if(TIFR & _BV(OCF0A))
	{
		TIFR = _BV(OCF0A);
		ms_counter++;
		idle_counter++;
		led_counter++;
	}
}

void key_report_poll( void ) {
	
	if(keys_changed)
	{
		// Change keys
		usb_load_endp1(keys, sizeof(keys));
		idle_counter = 0;
		keys_changed = 0;
		DDR_SET(A, 1);
	}

	if(idle_rate && idle_counter >= (idle_rate * 4))
	{
		// Force a report
		usb_load_endp1(keys, sizeof(keys));
		idle_counter = 0;
	}
}

void spoof_fetch( void ) {
	spoof_desc = &spoofs[spoofs_index];
	spoof_events = (const spoof_event_entry *)pgm_read_word(((const byte_t *) spoof_desc) + offsetof(spoof, events));
	spoof_length = pgm_read_word(((const byte_t *) spoof_desc) + offsetof(spoof, length));
}

void spoofer_poll( void ) {
	int i;
	const byte_t *src;
	
	
	if(ms_counter >= spoof_delay && spoof_delay != -2) // loop forever on -2
	{
		spoof_delay = pgm_read_dword(&(spoof_events[spoof_index].delay));
		// should we skip to a new spoof description?
		if (spoof_delay == -1) { // yes
			spoof_delay = random() & 0xFFFF;
			ms_counter = 0;
			spoof_index = 0;
			spoofs_index = random() & 0x3;
			spoof_fetch();
		} else { // no
			src = spoof_events[spoof_index].keys;
			for(i = 0; i < sizeof(keys); ++i) {
				keys[i] = pgm_read_byte(src);
				src++;
			}
			ms_counter = 0;
			keys_changed = 1;
			spoof_index++;
			if (spoof_index >= spoof_length) {
				spoof_index = 0;
			}
		}
	}
}

void spoof_init( void ) {
	spoof_fetch();
}


// ----------------------------------------------------------------------
// Main
// ----------------------------------------------------------------------
extern	int	main ( void )
{
	PORT_SET(A, 1);
	DDR_SET(A, 0);
	usb_init();
	spoof_init();
	timer0_init();
	for	( ;; )
	{
		usb_poll();
	    timer0_poll();
	    key_report_poll();
	    spoofer_poll();
	
		if (led_counter >= LED_PULSE_DELAY) {
			DDR_CLR(A, 1);
			led_counter = 0;
		}
	}
}
